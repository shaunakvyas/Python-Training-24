# Prompt the user to enter two numbers between 1-10
num1 = int(input("Enter the first number (1-10): "))
num2 = int(input("Enter the second number (1-10): "))

# Add the two numbers and store the sum in variable z
z = num1 + num2

# Add 30 to z and store the result in variable result
result = z + 30

# Print the final result
print("Result: ", result)
