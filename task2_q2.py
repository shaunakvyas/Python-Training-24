# complex variable
complex_num = 3 + 2j
print("Original complex number:", complex_num)

# integer variable
integer_num = 10
print("Original integer number:", integer_num)

# Swapping the values
temp = complex_num
complex_num = integer_num
integer_num = temp

print("After swapping:")
print("Complex number:", complex_num)
print("Integer number:", integer_num)
